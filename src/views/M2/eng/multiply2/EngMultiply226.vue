<p class="up">Jesus is shifting to a new period of the movement. The ministry and momentum were growing and people from all over Israel were believing in Him as Messiah. It is at this crucial time that Jesus issues a call for His disciples to gather. As they are gathering, Jesus goes up on a mountainside to pray, modeling for us the first principle of leadership development &ndash; dependency on God. Fresh from prayer, Jesus comes down the mountain to a level place and out of many disciples (maybe 100 or more) He publicly appoints the first 12 movement leaders. He calls them apostles, which means sent ones. This is the second principle of leadership, public appointment. In this principle, leaders are selected from a wider group of disciples who have been trained and have experience in fishing for men. He selected them so that they would be with Him (personal development) and that He would send them out to do the things He had been doing (authority for ministry).</p>

